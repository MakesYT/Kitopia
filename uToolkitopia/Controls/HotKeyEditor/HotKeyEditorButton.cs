namespace Kitopia.Controls.HotKeyEditor
{
    /// <summary>
    /// Defines constants that specify the default button on a <see cref="HotKeyEditor"/>.
    /// </summary>
    public enum HotKeyEditorButton
    {
        /// <summary>
        /// The primary button is the default.
        /// </summary>
        Primary,

        /// <summary>
        /// The secondary button is the default.
        /// </summary>
        Secondary,

        /// <summary>
        /// The close button is the default.
        /// </summary>
        Close
    }
}